import UIKit

extension UIColor {
	class func textFieldPlaceholderColor() -> UIColor {
		let newColor = UIColor(red: 200.0/255.0, green: 200.0/255.0, blue: 205.0/255.0, alpha: 1.0)
		return newColor
	}
}
